package cn.edu.zut.model;

public class Student {
    private String id;
    private String name;
    private String sex;
    private int grade;
    public String getId() {
        return id;
    }
    public void setid(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getSex() {
        return name;
    }
    public void setsex(String sex) {
        this.sex = sex;
    }
    public int getGrade() {
        return grade;
    }
    public void setGrade(int grade) {
        this.grade = grade;
    }

}
